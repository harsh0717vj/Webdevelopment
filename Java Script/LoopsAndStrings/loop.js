//for-loop
for(let i=1;i<=10;i++){
    console.log('harsh');
}
for(let i=1;i<=5;i++){
    console.log(i);
}
for(let i=5;i>=0;i--){
    console.log(i);
}
for(let i=5;i>=0;i--){
    if(i==2) break;
    else console.log(i);
}
for(let i=5;i>=0;i--){
    if(i==2) continue;
    else console.log(i);
}
//while-loop
let i=1;
while(i<=10){
    console.log(i);
    i++;
}
//do-while loop
let j=1;
do{
    console.log(j);
    j++;
}while(j<=10);
